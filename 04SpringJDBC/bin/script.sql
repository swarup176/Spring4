CREATE TABLE Student(
   ID   INT NOT NULL ,
   NAME VARCHAR(20) NOT NULL,
   AGE  INT NOT NULL,
   PRIMARY KEY (ID)
);
CREATE SEQUENCE id_seq START WITH 1;

CREATE OR REPLACE TRIGGER id_populate 
BEFORE INSERT ON Student 
FOR EACH ROW

BEGIN
  SELECT id_seq.NEXTVAL
  INTO   :new.id
  FROM   dual;
END;
/